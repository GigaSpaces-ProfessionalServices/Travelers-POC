-- Create tables 
-- CUSTOMER
CREATE TABLE TRAV.CUSTOMER (
    ID INTEGER NOT NULL,
    NAME VARCHAR(100),
    CUSTOMER_EMAIL    VARCHAR(100),
    CREATEDDATE DATE,
    PRIMARY KEY(ID)
) IN TRAVDB.TRAVTS
CCSID EBCDIC;

CREATE UNIQUE INDEX TRAV.IDKEY
 ON TRAV.CUSTOMER(ID)
 USING STOGROUP TRAVSG;
 
-- PRODUCT
CREATE TABLE TRAV.PRODUCT (
    PRODUCT_ID        INTEGER         NOT NULL,
    PRODUCT_NAME      VARCHAR(100)    NOT NULL,
    DESCRIPTION       VARCHAR(255),
    CATEGORY          VARCHAR(50),
    MANUFACTURER      VARCHAR(100),
    PRICE             DECIMAL(10,2)   NOT NULL,
    COST              DECIMAL(10,2),
    STOCK_QUANTITY    INTEGER         NOT NULL,
    SKU               VARCHAR(30),
    CREATED_AT        DATE            NOT NULL,
    UPDATED_AT        DATE            NOT NULL,
    PRIMARY KEY (PRODUCT_ID)
) IN TRAVDB.TRAVTS
CCSID EBCDIC;

CREATE UNIQUE INDEX TRAV.PRODUCT_PK
 ON TRAV.PRODUCT(PRODUCT_ID)
 USING STOGROUP TRAVSG;

-- ORDERS
CREATE TABLE TRAV.ORDERS (
    ORDER_ID          INTEGER         NOT NULL,
    PRODUCT_ID        INTEGER         NOT NULL,
    CUSTOMER_ID       INTEGER         NOT NULL,
    SHIPPING_ADDRESS  VARCHAR(255),
    BILLING_ADDRESS   VARCHAR(255),
    ORDER_DATE        DATE            NOT NULL,
    DELIVERY_DATE     DATE,
    ORDER_STATUS      VARCHAR(20),
    QUANTITY          INTEGER         NOT NULL,
    TOTAL_AMOUNT      DECIMAL(12,2),
    PAYMENT_METHOD    VARCHAR(50),
    CREATED_AT        DATE            NOT NULL,
    UPDATED_AT        DATE            NOT NULL,
    PRIMARY KEY (ORDER_ID)
) IN TRAVDB.TRAVTS
CCSID EBCDIC;

CREATE UNIQUE INDEX TRAV.ORDERS_PK
 ON TRAV.ORDERS(ORDER_ID)
 USING STOGROUP TRAVSG;



